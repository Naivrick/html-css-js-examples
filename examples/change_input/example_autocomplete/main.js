const input_text = document.querySelector(".input_text");
const output = document.querySelector(".suggestions");

input_text.addEventListener("input", () => {
    const query = input_text.value.toLowerCase();
    output.innerHTML = ""; // Clear previous suggestions

    if (query) {
        const filteredItems = streets.filter((item) =>
            item.toLowerCase().includes(query)
        );
        filteredItems.forEach((item) => {
            const div = document.createElement("div");
            div.textContent = item; // Set the text content of the div to the item

            div.addEventListener("click", () => {
                input_text.value = item; // Set the input value to the clicked suggestion
                output.innerHTML = ""; // Clear suggestions after selection
            });
            output.appendChild(div);
        });
    }
});

